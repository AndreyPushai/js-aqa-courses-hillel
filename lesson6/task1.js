
// Declaration
function getRectangleArea(width, height) {
    return width * height;
}

console.log(getRectangleArea(5, 10));

// Expression
const rectangleArea = function (width, height) {
    return width * height;
}

console.log(rectangleArea(51, 25));

// Arrow
const arrowRectangleArea = (width,height) => {
    return width * height;
}

console.log(arrowRectangleArea(18, 44));

